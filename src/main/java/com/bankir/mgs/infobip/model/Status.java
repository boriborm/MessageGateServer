package com.bankir.mgs.infobip.model;

public class Status {

    private int id;
    private int groupId;
    private String name;
    private String description;
    private String groupName;

    public int getId() {
        return id;
    }

    public int getGroupId() {
        return groupId;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getGroupName() {
        return groupName;
    }
}
